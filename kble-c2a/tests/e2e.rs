/// A single complete frame in, the same frame out.
#[tokio::test]
async fn tfsync_emits_a_single_complete_frame() {
    let mut plug = Plug::spawn(c2a(&["tfsync"]))
        .await
        .expect("spawn kble-c2a tfsync");
    plug.send(Bytes::copy_from_slice(&TRANSFER_FRAME))
        .await
        .expect("send frame");
    let frame = plug.recv().await.expect("tfsync emits the frame");
    plug.shutdown().await.expect("tfsync exits cleanly");

    assert_eq!(frame.as_ref(), TRANSFER_FRAME.as_slice());
}

/// Two frames back-to-back in one chunk are emitted as two separate frames.
#[tokio::test]
async fn tfsync_splits_contiguous_frames() {
    let input = [TRANSFER_FRAME, TRANSFER_FRAME].concat();

    let mut plug = Plug::spawn(c2a(&["tfsync"]))
        .await
        .expect("spawn kble-c2a tfsync");
    plug.send(Bytes::from(input))
        .await
        .expect("send two contiguous frames");
    let first = plug.recv().await.expect("first frame");
    let second = plug.recv().await.expect("second frame");
    plug.shutdown().await.expect("tfsync exits cleanly");

    assert_eq!(first.as_ref(), TRANSFER_FRAME.as_slice());
    assert_eq!(second.as_ref(), TRANSFER_FRAME.as_slice());
}

/// Leading junk before the first header is skipped; the frame still comes out.
#[tokio::test]
async fn tfsync_resyncs_past_leading_junk() {
    let mut input = b"JUNKDATA".to_vec();
    input.extend_from_slice(&TRANSFER_FRAME);

    let mut plug = Plug::spawn(c2a(&["tfsync"]))
        .await
        .expect("spawn kble-c2a tfsync");
    plug.send(Bytes::from(input))
        .await
        .expect("send junk + frame");
    let frame = plug.recv().await.expect("tfsync resyncs to the frame");
    plug.shutdown().await.expect("tfsync exits cleanly");

    assert_eq!(frame.as_ref(), TRANSFER_FRAME.as_slice());
}

